/**
 * @file  Fuzzy_Pid.h
 * @brief 模糊PD位置环控制器 — 逐飞RC380 + 1024线正交编码器
 *
 * ─────────────────────────────────────────────────────────────────
 * ─────────────────────────────────────────────────────────────────
 * ─────────────────────────────────────────────────────────────────
 * ─────────────────────────────────────────────────────────────────
 * ─────────────────────────────────────────────────────────────────
 * ─────────────────────────────────────────────────────────────────
 *  [v7 修订摘要 — 长行程稳态精度提升]
 *
 *  问题: v6 在 4096 短行程下表现良好,但 40960 等长行程目标值下
 *        位置在目标附近 ±1100 脉冲低频漂移,纯 PD 无法消除累积
 *        扰动(温漂、电源纹波、机械负载变化)。
 *
 *  根因: 单环位置 PD 没有积分项,持续小幅度扰动无法被控制器
 *        "记住"并补偿。死区锁存把表象遮住,但稍有大扰动出区后
 *        系统又重新被推开。
 *
 *  v7 解决方案: 加入积分项,变成完整模糊 PID(单环)
 *    ① 条件积分: 仅 |e| < I_RANGE (=300脉冲) 时累加,远端不累
 *       避免大误差段 I 项 windup 导致严重超调
 *    ② Anti-windup: 输出限幅时本周期不累加,防止积分失控
 *    ③ 锁定清零: 死区锁定状态下 I 强制清零,解锁后重新累积
 *    ④ Ki 单值常数(不模糊整定): 减少调参维度,实际效果差异极小
 *
 *  v7 调参提示:
 *    FUZZY_KI 主要作用:消除稳态静差
 *      过小  → 静差消除慢甚至消除不掉
 *      过大  → 在小误差段引发超调振荡
 *      推荐 0.005~0.05,从小往大调
 *    FUZZY_I_RANGE 决定"积分起作用区域":
 *      过小  → 静差始终消不掉(I 还没启动就锁定了)
 *      过大  → 大误差段 windup 严重
 *      推荐 100~500 脉冲
 *
 *  注: 长行程(>10圈)精度极致追求建议升级到级联PID,
 *      单环 PID 即便加 I 也有理论瓶颈。
 * ─────────────────────────────────────────────────────────────────
 *
 *  [v6 修订摘要 — 针对 v5 持续不衰减极限环问题]
 *
 *  问题: v5 实测出现长周期(18~22采样点)持续锯齿振荡,
 *        位置在目标附近 ±300 脉冲反复横跳,PWM 跟随锯齿,
 *        从未自然衰减,即使运行 700+ 周期仍持续。
 *
 *  根因: 单一阈值死区 + 死区内重置 ec_filt 导致冷启动效应:
 *    ① |e| < 8 进死区, output=0, error_last=e, ec_filt=0
 *    ② 外部小扰动让位置漂到 |e|=10,出死区
 *    ③ 出区瞬间 ec_filt=0 让模糊误判"系统静止",前馈冷启动 220 满火力
 *    ④ 电机一脚踢出,过冲到反向 → 反向重复
 *    ⑤ 形成稳定极限环,与摩擦/前馈/死区宽度的物理参数共振
 *
 *  v6 解决方案: 死区状态锁存(Latching Deadband)
 *    ① 进区阈值 DB_ENTER=8, 出区阈值 DB_EXIT=30 (滞回机制)
 *       小幅扰动(8~30脉冲)无法触发输出
 *    ② 进入死区后还需连续 DB_HOLD_CYCLES=5 个周期保持稳定才"锁定"
 *       防止误进入(瞬时穿越死区)
 *    ③ 锁定状态下 output 恒 0,直到 |e| > DB_EXIT 才解锁
 *    ④ 死区内不再重置 ec_filt,保留滤波器状态,出区时无冷启动
 *
 *  v6 调参提示:
 *    DB_EXIT 决定"扰动免疫力":
 *      过小  → 仍有小幅锯齿
 *      过大  → 抗扰能力强但精度松弛 (死区从±8→±EXIT)
 *      推荐 20~40 脉冲,根据机械工况选
 *    DB_HOLD_CYCLES 决定"锁定灵敏度":
 *      过小  → 路径中瞬时穿越死区也会被锁
 *      过大  → 真到位时锁不上
 *      推荐 3~10 (10ms周期下对应 30~100ms 静止时间)
 * ─────────────────────────────────────────────────────────────────
 *
 *  [v5 修订摘要 — 针对 v4 持续小幅锯齿振荡问题]
 *
 *  问题: v4 远端表现良好(平滑爬升,无阶梯),但稳态前 200+ 周期
 *        在目标附近持续 ±200 脉冲锯齿振荡,周期约 5~8 采样点。
 *
 *  根因 (两点叠加):
 *    ① E_FF_FULL=80 衰减区间过宽:
 *       e 从 80→20 之间前馈仍有 220→55,大部分时段高于电机静摩擦
 *       阈值 (~180),导致系统在"刚好能动/刚好不能动"边界反复跨越,
 *       而非平滑减速到死区。
 *    ② ec 一阶滤波 α=0.7 时间常数 3.3 周期过大:
 *       Kd 反馈相位滞后过长,与机械响应耦合形成自激振荡,
 *       恰好对应实测中 5~8 周期的振荡频率。
 *
 *  v5 修改清单:
 *    [1] FUZZY_E_FF_FULL: 80 → 40  (前馈衰减曲线变陡 2 倍)
 *    [2] ec 滤波 α:       0.7 → 0.5 (滤波时间常数 3.3→2 周期)
 *
 *  v5 调参提示:
 *    若仍有小幅锯齿:
 *      - 进一步减小 E_FF_FULL (40 → 30)
 *      - 进一步减小滤波 α    (0.5 → 0.3)
 *    若远端响应变迟缓:
 *      - 不要动 E_FF_FULL,改加大 FRICTION_FF
 * ─────────────────────────────────────────────────────────────────
 *
 *  [v4 修订摘要 — 针对 v3 前馈过冲振荡问题]
 *
 *  问题: v3 前馈是固定常数 220,在小误差段电机起动后会一脚跨过
 *        死区到反向,触发 ±400 脉冲的反复过冲,虽然最终能收敛
 *        但 50+ 周期持续振荡,PWM 贴限幅来回切换。
 *
 *  根因: 固定前馈在小误差区造成"最小步长 > 死区宽度"。
 *        e=80 时模糊P项仅约4 PWM,但前馈直接加 220,一步走过头。
 *
 *  v4 修改清单:
 *    [1] 前馈改为渐进式 (errror-proportional feedforward):
 *        ff = FRICTION_FF × min(|e|/E_FF_FULL, 1.0)
 *        |e| ≥ E_FF_FULL → 前馈满量程 (远端顶开摩擦)
 *        |e| <  E_FF_FULL → 前馈按比例线性减小 (近端温柔接近)
 *        |e| <  DEADBAND  → 前馈失效 (死区强制归零)
 *    [2] 新增宏 FUZZY_E_FF_FULL = 100 脉冲 (前馈满量程切换点)
 *
 *  v4 调参提示:
 *    FRICTION_FF 含义不变 (静摩擦启动 PWM,典型 150~300)
 *    E_FF_FULL 决定"温柔接近"区域宽度:
 *      过小 → 前馈衰减太晚,接近 v3 行为,小幅过冲
 *      过大 → 远端前馈不够,接近 v2 行为,有静差
 *      推荐 50~150,跟死区宽度形成 6~20 倍比例最稳
 * ─────────────────────────────────────────────────────────────────
 *
 *  [v3 修订摘要 — 针对 v2 稳态静差问题]
 *
 *  问题: v2 极限环已消除,但稳态停在偏离目标约 +54 脉冲处
 *        (实测目标4096,实际4150)。电机PWM归零前卡在静摩擦
 *        启动阈值以下,无法继续微调。
 *
 *  根因: 纯 PD 在含静摩擦执行器上的固有静差。e=54时 P项≈68 PWM,
 *        而 RC380+齿轮箱的静摩擦启动 PWM 约 200~300,电机无法起步。
 *
 *  v3 修改清单:
 *    [1] 新增摩擦前馈 FUZZY_FRICTION_FF = 220
 *        在死区外按误差符号给 PWM 加固定偏置,使电机能持续微调
 *        直到进入死区。前馈在限幅之前叠加,在死区内自动失效。
 *
 *  v3 调参提示:
 *    FUZZY_FRICTION_FF 必须根据实测调整:
 *      - 电机最终稳态在死区外 (静差 > 8脉冲) → 前馈不够,加大
 *      - 电机在目标附近来回过冲振荡        → 前馈过大,减小
 *      - RC380 + 1024线齿轮电机典型值 150~300
 * ─────────────────────────────────────────────────────────────────
 *
 *  [v2 修订摘要 — 针对 v1 极限环振荡问题]
 *
 *  问题: v1 在目标 4096 脉冲附近产生 ±500 脉冲、PWM 贴限的
 *        持续极限环 (bang-bang 振荡),不能自行收敛。
 *
 *  根因:
 *    ① KP_MIN=0.5 + ZO 行 Kp_level 最低 1,小误差段 P 项强制饱和
 *    ② EC_SCALE=333.3 远大于实际 ec 量级,Kd 在小误差段几乎不工作
 *    ③ fuzzify 截断取整使负小误差不对称归类
 *    ④ 无位置死区,齿轮箱回程间隙引起持续抖动
 *
 *  v2 修改清单 (按影响排序):
 *    [1] FUZZY_KP_MIN: 0.5 → 0.05
 *    [2] FUZZY_EC_SCALE: 333.3 → 33.3
 *    [3] Kp_Rule ZO行: {2,2,2,1,2,2,2} → {1,1,0,0,0,1,1}
 *    [4] 新增 FUZZY_DEADBAND_PULSE = 8 脉冲位置死区
 *    [5] fuzzify 改用四舍五入 floorf(fv+3.5)
 *    [6] 新增 ec_filt 一阶低通 (α=0.7),抑制编码器量化噪声经
 *        微分被 Kd 放大的高频抖动
 *
 *  v2 已知遗留问题 (后续优化方向):
 *    - 纯 PD 在含静摩擦的电机上接近目标时会出现 ~200 脉冲的
 *      稳态静差。如需消除,可:
 *        (a) 加积分项构成完整模糊PID
 *        (b) 加摩擦前馈 u_ff = sign(e) * STATIC_FRIC_PWM
 *        (c) 应用层接受死区精度 (±8脉冲约 ±0.7°,通常已够用)
 * ─────────────────────────────────────────────────────────────────
 *
 *  控制结构: 单环位置PD，无积分项
 *
 *    目标位置(脉冲数)
 *         │
 *         ▼
 *    [误差计算] ← 编码器累计脉冲 (Ecoder_Total_Lx/Rx)
 *         │  e = target - actual
 *         │  ec = e(k) - e(k-1)
 *         ▼
 *    [模糊推理] — Kp_Rule[7][7], Kd_Rule[7][7]
 *         │  Kp = f(e, ec) ∈ [0.5, 5.0]
 *         │  Kd = g(e, ec) ∈ [0.05, 1.5]
 *         ▼
 *    [PD 计算]
 *         │  u = Kp×e + Kd×ec
 *         │  u = clamp(u, -8000, +8000)
 *         ▼
 *    [Motor_Set] → 方向GPIO + PWM占空比 → RC380电机
 * ─────────────────────────────────────────────────────────────────
 *  编码器分辨率: 1024线 × 4倍频 = 4096 脉冲/转
 * ─────────────────────────────────────────────────────────────────
 *  标准调用顺序 (在定时中断中，推荐周期 5~20 ms):
 *
 *    void pit0_isr(void)
 *    {
 *        Encoder_Speed_PID_Update();    // ① 更新编码器
 *        Fuzzy_PID_Calculate(0xFF);     // ② 模糊PD计算
 *        Fuzzy_PID_Drive_All_Motor();   // ③ 驱动电机
 *    }
 * ─────────────────────────────────────────────────────────────────
 *  调参速查:
 *    响应太慢  → 增大 FUZZY_KP_MAX (当前 5.0)
 *    有超调    → 增大 FUZZY_KD_MAX (当前 1.5) 或减小 FUZZY_KP_MAX
 *    目标附近抖动 → 增大 FUZZY_KD_MAX 或在应用层加位置死区
 *    大误差全速段太短 → 减小 FUZZY_E_SCALE (扩大满量程范围)
 */

#ifndef FUZZY_PID_H
#define FUZZY_PID_H

#include "zf_common_headfile.h"

/* =====================================================================
 *  电机通道 ID
 *
 *  用于所有函数的 id 参数，指定操作哪路电机。
 *  0xFF 是广播值，表示"全部4路"，仅 Fuzzy_PID_Calculate 和
 *  Clear_Motor_Position 支持。
 * ===================================================================== */
#define FUZZY_POS_PID_L1    0u   /**< 左前电机 (Left-Front)  */
#define FUZZY_POS_PID_L2    1u   /**< 左后电机 (Left-Rear)   */
#define FUZZY_POS_PID_R1    2u   /**< 右前电机 (Right-Front) */
#define FUZZY_POS_PID_R2    3u   /**< 右后电机 (Right-Rear)  */

/* =====================================================================
 *  编码器参数
 * ===================================================================== */
#define ENCODER_RESOLUTION  1024u
/**< 每转脉冲数: 1024线 × 4倍频(A+B正交) = 4096 脉冲/转 */
#define PULSE_PER_REV       (ENCODER_RESOLUTION * 4u)

/* =====================================================================
 *  模糊量化参数
 *
 *  量化公式: fuzzy_val = clamp(精确值 / SCALE, -3.0, +3.0)
 *  量化索引: idx = (int)(fuzzy_val + 3.0)  ∈ [0, 6]
 *
 *  FUZZY_E_SCALE = 833.3:
 *    ±2500 脉冲以内按7级线性量化，超出则限幅到最大等级(NB/PB)，
 *    仍能输出最大 Kp 驱动电机，不影响大误差时的全速响应。
 *    覆盖约 ±0.6 转，适合精确定位场景。
 *    若需要更大行程的精细调节，可增大此值。
 *
 *  FUZZY_EC_SCALE = 33.3:  [v2修正]
 *    ±100 脉冲/周期的误差变化率按7级量化。
 *    1024线编码器10ms周期下,稳态段 ec 通常在 ±5~50 范围,
 *    旧值 333.3 导致 ec_idx 长期卡在 ZO,Kd 在小误差段几乎不工作。
 *    降至 33.3 后,±100 脉冲/周期即可让 Kd 走到满量程,有效抑制振荡。
 * ===================================================================== */
#define FUZZY_SET_SIZE      7u        /**< 模糊等级数 (NB NM NS ZO PS PM PB) */
#define FUZZY_E_SCALE       833.3f    /**< 误差量化比例 (±2500脉冲 → ±3.0)  */
#define FUZZY_EC_SCALE      33.3f     /**< 误差变化率量化比例 (±100 → ±3.0)  [v2: 333.3→33.3] */

/* =====================================================================
 *  PD 参数范围
 *
 *  BASE 值为初始值（上电后写入控制器），之后每周期由模糊推理覆盖。
 *  MIN/MAX 是模糊推理输出的物理限幅，防止增益过小（响应迟钝）或
 *  过大（振荡失稳）。
 *
 *  Kp 物理含义:
 *    u = Kp × e，当 e = PWM_MAX/Kp_MAX 时输出饱和。
 *    Kp_MAX=5.0 时，e≥1600脉冲(约0.4转)即全速。
 *    Kp_MIN=0.5 时，e≥16000脉冲(约4转)才全速（细腻控制区间更宽）。
 *
 *  Kd 物理含义:
 *    Kd × ec 提供阻尼力，ec 为每周期误差变化量（近似速度）。
 *    Kd_MAX=1.5 时，ec=500脉冲/周期 贡献 750 PWM（约9%满量程）。
 * ===================================================================== */
#define FUZZY_KP_BASE       2.0f    /**< Kp 初始值                    */
#define FUZZY_KD_BASE       0.3f    /**< Kd 初始值                    */
#define FUZZY_KP_MIN        0.05f   /**< Kp 模糊输出下限 [v2: 0.5→0.05] */
#define FUZZY_KP_MAX        5.0f    /**< Kp 模糊输出上限               */
#define FUZZY_KD_MIN        0.05f   /**< Kd 模糊输出下限               */
#define FUZZY_KD_MAX        1.5f    /**< Kd 模糊输出上限               */

/* =====================================================================
 *  积分项参数 [v7新增]
 *
 *  作用: 消除纯 PD 在长持续扰动下的稳态静差。
 *
 *  设计选择:
 *    Ki 不参与模糊整定 — 设为固定常数,简化调参
 *    Ki 项的贡献 = Ki × Σe × dt,其中 dt 已隐含在采样周期里
 *
 *  数值范围:
 *    0.001 ~ 0.005  — 弱积分,主要靠 P 收敛,适合短行程
 *    0.005 ~ 0.020  — [v7默认] 中等积分,平衡静差消除速度和稳定性
 *    0.020 ~ 0.050  — 强积分,长行程必备,但调参余量小
 *    > 0.05         — 极易振荡,不推荐
 *
 *  I_RANGE: 条件积分阈值
 *    仅 |e| < I_RANGE 时积分项才累加。
 *    避免大误差段 P 已饱和限幅,I 还在堆积造成"积分饱和(windup)"
 *    导致进入小误差区后严重超调。
 * ===================================================================== */
#define FUZZY_KI            0.010f      /**< 积分系数 (固定值,不模糊整定) */
#define FUZZY_I_RANGE       300.0f      /**< 积分起作用的误差范围 (脉冲)   */
#define FUZZY_I_OUT_MAX     400.0f      /**< 积分项贡献的 PWM 上限         */

/* =====================================================================
 *  位置死区 [v2新增]
 *
 *  当 |e| < FUZZY_DEADBAND_PULSE 时直接输出 0,避免:
 *    - RC380 齿轮箱回程间隙(典型 ±2~10 脉冲)导致的小幅抖动
 *    - 静摩擦下"推不动—猛推—冲过—反向"极限环
 *  8 脉冲 ≈ 0.7°,符合大多数定位精度要求。如需更高精度,请配合
 *  电机刹车策略或前馈补偿,而不是简单缩小死区。
 * ===================================================================== */
/* =====================================================================
 *  死区与锁存机制 [v2引入死区, v6改为状态锁存]
 *
 *  状态机两态:
 *    DZ_INACTIVE - 正常控制态, 执行 PD + 前馈
 *    DZ_ACTIVE   - 锁定态, 输出强制 0
 *
 *  状态转移:
 *    INACTIVE → ACTIVE :  连续 DB_HOLD_CYCLES 个周期 |e| < DB_ENTER
 *    ACTIVE → INACTIVE :  |e| > DB_EXIT (单周期触发)
 *
 *  滞回宽度 = DB_EXIT - DB_ENTER, 物理含义为"扰动免疫带":
 *    锁定后 8~30 脉冲范围的位置漂移被完全忽略,不会唤醒电机。
 * ===================================================================== */
#define FUZZY_DB_ENTER          8       /**< 进死区阈值 (脉冲)              */
#define FUZZY_DB_EXIT          30       /**< 出死区阈值 (脉冲, 必须>ENTER)  */
#define FUZZY_DB_HOLD_CYCLES    5       /**< 进死区持续周期 (10ms周期=50ms) */

/* 兼容旧宏: 部分用户代码可能引用 FUZZY_DEADBAND_PULSE,保留为 ENTER 别名 */
#define FUZZY_DEADBAND_PULSE  FUZZY_DB_ENTER

/* =====================================================================
 *  PWM 变化率限制 (Slew Rate Limit) [v6新增]
 *
 *  目的: 限制相邻周期 PWM 变化幅度,实现"输出无突变",
 *        避免对电机驱动器、电流回路、机械传动产生冲击。
 *
 *  机制: u(k) = u_pd_raw, 但若 |u(k) - u(k-1)| > MAX_DELTA,
 *        则 u(k) = u(k-1) ± MAX_DELTA
 *
 *  数值参考 (10ms 周期, PWM_MAX=1000):
 *    100  — 强限速,从0到满量程需 100ms (10步)
 *    200  — [v6默认] 中等限速,50ms 到满量程
 *    400  — 弱限速,25ms 到满量程
 *    >=2000  — 等同关闭限速 (单周期就能从+1000跳到-1000)
 *
 *  调参提示:
 *    电机/驱动器有冲击声/抖动 → 减小 MAX_DELTA
 *    响应迟缓 / 大误差段动力不足 → 加大 MAX_DELTA
 *
 *  注意: 本机制会让"瞬时大误差跟踪能力"略下降, 用 200 是平衡点。
 *        如果应用对响应速度极其敏感, 设为 1000 (本周期允许最大跳变)
 *        即可关闭此功能。
 * ===================================================================== */
#define FUZZY_PWM_SLEW_MAX    200.0f

/* =====================================================================
 *  ec 一阶低通滤波系数 [v2 引入,v5 提为宏]
 *
 *  滤波公式: ec_filt = α × ec_filt + (1-α) × ec_raw
 *  时间常数(周期数): τ ≈ 1/(1-α)
 *
 *  v5 默认 0.5 (τ=2 周期):
 *    比 v2 的 0.7 (τ=3.3) 更小,降低 Kd 反馈相位滞后,
 *    避免与机械响应耦合形成自激振荡。
 *
 *  调参参考:
 *    0.0  — 不滤波,纯 ec_raw,适合无量化噪声的理想电机(实际不存在)
 *    0.3  — 弱滤波,响应最快,但易受编码器噪声影响
 *    0.5  — [v5默认] 平衡点,适合 1024 线编码器 + 10ms 周期
 *    0.7  — [v2默认] 强滤波,适合低分辨率编码器或高噪声环境
 *    0.9  — 极强滤波,Kd 几乎失效,不推荐
 * ===================================================================== */
#define FUZZY_EC_FILTER_ALPHA  0.5f

/* =====================================================================
 *  摩擦前馈 [v3新增, v4改为渐进式]
 *
 *  作用机制 (v4):
 *    在死区外按误差大小渐进叠加 PWM 偏置:
 *      ff = FRICTION_FF × clamp(|e| / E_FF_FULL, 0, 1)
 *    符号跟随误差方向。物理含义:
 *      远端 (|e| ≥ E_FF_FULL): 满量程前馈,顶开静摩擦,与 v3 等效
 *      近端 (|e| <  E_FF_FULL): 前馈按比例减弱,避免一步跨过死区
 *
 *  叠加位置: pd_step 中死区判断之后、输出限幅之前
 *  进入死区后 output 被强制为 0,前馈自动失效。
 *
 *  数值参考:
 *    FRICTION_FF: 150~300 — RC380 + 普通齿轮箱常见区间
 *    E_FF_FULL:   50~150  — "温柔接近"区域宽度
 *    设 FRICTION_FF=0 即关闭前馈 (退化为 v2)
 *
 *  调参逻辑:
 *    稳态有静差 (停在死区外)        → 加大 FRICTION_FF
 *    接近目标时小幅过冲来回         → 减小 FRICTION_FF 或加大 E_FF_FULL
 *    远端响应迟缓 / 阶梯式爬升明显   → 减小 E_FF_FULL
 * ===================================================================== */
#define FUZZY_FRICTION_FF     220
#define FUZZY_E_FF_FULL       35.0f       /**< [v4新增, v5: 80→35] 前馈满量程的误差阈值 */

/* =====================================================================
 *  输出限幅
 *
 *  Motor_Set() 接受 [-10000, +10000] 对应 0%~100% 占空比。
 *  此处设为 8000（80%），预留 20% 余量给电机启动过冲，
 *  避免长时间满占空比损伤驱动器。
 * ===================================================================== */
#define FUZZY_PWM_MAX_OUT   1000.0f

/* =====================================================================
 *  数据结构
 * ===================================================================== */

/**
 * @brief 单路模糊PD控制器实例
 *
 * 每路电机对应一个实例（pd_L1/pd_L2/pd_R1/pd_R2），
 * 所有字段由控制函数自动管理，外部通常无需直接访问。
 *
 * 字段说明:
 *   Kp, Kd          每周期由 fuzzy_adjust_gains() 写入新值
 *   error_last       上周期误差，用于计算 ec = e(k)-e(k-1)
 *   output           上次 pd_step() 计算的 PWM 输出值
 *   pwm_max          输出限幅，固定为 FUZZY_PWM_MAX_OUT
 *   target_position  目标脉冲数，由 Fuzzy_PID_Set_Target() 写入
 *   actual_position  当前脉冲数，由 Fuzzy_PID_Calculate() 从编码器读入
 *   motor_id         通道号，用于调试识别
 *   dir_pin          方向控制 GPIO，传给 Motor_Set()
 */
typedef struct {
    float   Kp;
    float   Kd;
    float   error_last;
    float   ec_filt;          /**< [v2] ec 一阶低通滤波器状态值       */
    float   integral;         /**< [v7] 积分项累加器                  */
    float   output;
    float   pwm_max;
    int32   target_position;
    int32   actual_position;
    uint8   motor_id;
    uint8   dir_pin;
    uint8   db_active;        /**< [v6] 死区锁定状态: 0=正常 1=锁定   */
    uint8   db_steady_count;  /**< [v6] 连续在死区内的周期计数        */
} Fuzzy_PD_Controller;

/**
 * @brief 兼容旧接口的输出结构
 *
 * 本版本为单环PD，无位置外环/速度内环级联结构，
 * position_out 始终为 0.0f，speed_out 为最终 PWM 输出值。
 * 保留此结构仅为向下兼容，直接使用 speed_out 即可。
 */
typedef struct {
    float position_out;  /**< 保留字段，始终为 0.0f              */
    float speed_out;     /**< 最终PWM输出值，范围 [-8000, +8000] */
} Cascade_PID_Output;

/* =====================================================================
 *  公开函数接口
 * ===================================================================== */

/**
 * @brief  初始化全部 4 路模糊PD控制器
 *
 * 将 pd_L1/L2/R1/R2 的增益、误差历史、输出值全部归零，
 * 并将 Kp/Kd 设为 BASE 值，绑定对应电机引脚。
 *
 * 调用要求:
 *   必须在 Motor_Init() 和 Encoder_Init() 之后调用。
 *   整个程序生命周期只需调用一次。
 *
 * 示例:
 *   void System_Init(void)
 *   {
 *       Motor_Init();
 *       Encoder_Init();
 *       MyPWM_Init();
 *       Fuzzy_PID_Init();   // 放在硬件初始化最后
 *   }
 */
void Fuzzy_PID_Init(void);

/**
 * @brief  设置指定电机的目标位置，并消除微分冲击
 *
 * 参数:
 *   @param id          电机通道 (FUZZY_POS_PID_L1/L2/R1/R2)
 *   @param target_pos  目标脉冲数 (相对于上次 Clear_Motor_Position 的零点)
 *                      正值=正转，负值=反转，1圈=4096脉冲
 *
 * 内部机制:
 *   将 error_last 预置为"当前误差"，使第一步 ec=0，
 *   防止目标突变时 Kd×ec 产生 PWM 突跳。
 *
 * 示例:
 *   Fuzzy_PID_Set_Target(FUZZY_POS_PID_L1, 4096);   // 正转1圈
 *   Fuzzy_PID_Set_Target(FUZZY_POS_PID_R1, -2048);  // 反转半圈
 */
void Fuzzy_PID_Set_Target(uint8 id, int32 target_pos);

/**
 * @brief  从外部写入当前位置（接入外部位置传感器时使用，可选）
 *
 * 正常使用内置编码器时无需调用，Fuzzy_PID_Calculate() 会自动
 * 从 Ecoder_Total_Lx/Rx 读取。
 *
 * @param id           电机通道
 * @param current_pos  外部传感器测量的当前位置 (脉冲数或等效单位)
 */
void Fuzzy_PID_Update_Position(uint8 id, int32 current_pos);

/**
 * @brief  模糊PD核心计算函数，在定时中断中周期调用
 *
 * 每次调用对选中通道完成:
 *   ① 读取编码器累计值 → actual_position
 *   ② 计算 e = target - actual，ec = e - error_last
 *   ③ 模糊推理更新 Kp、Kd
 *   ④ output = Kp×e + Kd×ec，限幅至 ±8000
 *   ⑤ error_last = e（保存供下周期使用）
 *
 * @param id  FUZZY_POS_PID_L1/L2/R1/R2 或 0xFF(全部4路)
 *
 * 注意:
 *   必须在 Encoder_Speed_PID_Update() 之后调用。
 *   建议调用周期 5~20 ms。
 *
 * 示例:
 *   void pit0_isr(void)
 *   {
 *       Encoder_Speed_PID_Update();
 *       Fuzzy_PID_Calculate(0xFF);
 *       Fuzzy_PID_Drive_All_Motor();
 *   }
 */
void Fuzzy_PID_Calculate(uint8 id);

/**
 * @brief  获取指定电机上次计算的 PD 输出（不触发重新计算）
 *
 * @param id  电机通道
 * @return    Cascade_PID_Output，.speed_out 为 PWM 值 ∈ [-8000, +8000]
 *            正值=正转，负值=反转
 *
 * 示例:
 *   float pwm = Fuzzy_PID_Get_Output(FUZZY_POS_PID_L1).speed_out;
 */
Cascade_PID_Output Fuzzy_PID_Get_Output(uint8 id);

/**
 * @brief  将 PD 输出写入单路电机（方向GPIO + PWM）
 *
 * @param id  电机通道 (FUZZY_POS_PID_L1/L2/R1/R2)
 *
 * 注意: 必须在本周期 Fuzzy_PID_Calculate() 之后调用。
 */
void Fuzzy_PID_Drive_Motor(uint8 id);

/**
 * @brief  驱动全部 4 路电机（4次 Drive_Motor 的简化封装）
 *
 * 示例:
 *   Fuzzy_PID_Calculate(0xFF);
 *   Fuzzy_PID_Drive_All_Motor();
 */
void Fuzzy_PID_Drive_All_Motor(void);

/**
 * @brief  获取指定电机的编码器累计脉冲数（当前绝对位置）
 *
 * @param id  电机通道
 * @return    int32 脉冲数，4096 = 1圈，负值表示反转方向。
 *            传入无效 id 返回 0。
 *
 * 示例:
 *   // 到位检测 (±15脉冲死区 ≈ ±1.3°)
 *   if (abs(Get_Motor_Position(FUZZY_POS_PID_L1) - 4096) <= 15)
 *       printf("到位!\n");
 */
int32 Get_Motor_Position(uint8 id);

/**
 * @brief  清零指定电机的位置计数器及 PD 内部状态
 *
 * 清零内容: Ecoder_Total_Lx/Rx、error_last、output
 * 不清零:   target_position（需重新调用 Set_Target 设置新目标）
 *
 * @param id  电机通道 或 0xFF(清零全部4路)
 *
 * 示例:
 *   // 建立新零点后运动半圈
 *   Clear_Motor_Position(0xFF);
 *   Fuzzy_PID_Set_Target(FUZZY_POS_PID_L1, 2048);
 *
 * 注意:
 *   清零后若不重新 Set_Target，target_position 仍是旧值（相对新零点），
 *   可能导致电机向意外方向运动。
 */
void Clear_Motor_Position(uint8 id);

#endif /* FUZZY_PID_H */
