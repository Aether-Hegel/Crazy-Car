/*********************************************************************************************************************
 *                              麦克纳姆轮电机驱动模块
 *
 * 文件名称          Motor.c
 * 适用平台          RT1064DVL6A
 *
 * ============================================================================================
 *                            麦克纳姆轮运动学模型推导
 * ============================================================================================
 *
 * 一、坐标系定义 (右手坐标系, 俯视图)
 *
 *          +Y (车头方向, Forward)
 *           ↑
 *           |
 *           |
 *    ───────O───────→ +X (车身右侧, Right)
 *           |
 *           |
 *
 *    逆时针旋转为正方向 (+ω)
 *
 *
 * 二、麦克纳姆轮安装方式 (O型, 俯视图)
 *
 *     ┌────────────────────────┐
 *     │  \L1 ←───── L ──────→ R1/  │
 *     │   │                     │   │
 *     │   ↑                     ↑   │
 *     │   a                     a   │      a = 车心到前后轴的距离 (半轴距)
 *     │   ↓                     ↓   │      b = 车心到左右轮的距离 (半轮距)
 *     │   │          O          │   │
 *     │   ↑                     ↑   │
 *     │   a                     a   │
 *     │   ↓                     ↓   │
 *     │   │                     │   │
 *     │  /L2 ←───── L ──────→ R2\  │
 *     └────────────────────────┘
 *              ←── b ──→
 *
 *    "/" 和 "\" 表示滚子轴线方向 (与轮轴成45°角)
 *    O型安装: 前轮滚子轴线形成 "\" 和 "/", 后轮形成 "/" 和 "\"
 *    与X型相比, 前后轮的滚子方向互换
 *
 *
 * 三、逆运动学公式 (从底盘速度到各轮速度)
 *
 *    O型安装麦克纳姆轮逆运动学方程:
 *
 *      V_L1 = Vy - Vx - (a+b)·ω     (左前轮, 滚子角 -45°)
 *      V_R1 = Vy + Vx + (a+b)·ω     (右前轮, 滚子角 +45°)
 *      V_L2 = Vy + Vx - (a+b)·ω     (左后轮, 滚子角 +45°)
 *      V_R2 = Vy - Vx + (a+b)·ω     (右后轮, 滚子角 -45°)
 *
 *    其中:
 *      Vx    = 底盘X轴 (左右) 线速度, 正值向右
 *      Vy    = 底盘Y轴 (前后) 线速度, 正值向前
 *      ω     = 底盘旋转角速度, 正值逆时针 (俯视)
 *      a     = 车心到前/后轴距离
 *      b     = 车心到左/右轮距离
 *      (a+b) = 几何参数, 影响旋转时各轮的分配比例
 *
 *    简化说明:
 *      在PWM直接控制的场景下, 我们将 (a+b)·ω 合并为一个旋转分量 omega,
 *      不需要知道实际的 a、b 尺寸, 直接用 omega 表示旋转量的PWM当量。
 *      因此公式简化为:
 *
 *      V_L1 = vy - vx - omega
 *      V_R1 = vy + vx + omega
 *      V_L2 = vy + vx - omega
 *      V_R2 = vy - vx + omega
 *
 *
 * 四、运动合成验证表 (各参数符号与运动方向的对应关系)
 *
 *    ┌─────────────────┬────────┬────────┬────────┬────────┬────────┬────────┬────────┐
 *    │ 运动方向        │ vx     │ vy     │ omega  │ L1     │ R1     │ L2     │ R2     │
 *    ├─────────────────┼────────┼────────┼────────┼────────┼────────┼────────┼────────┤
 *    │ 前进            │  0     │  +     │  0     │  +     │  +     │  +     │  +     │
 *    │ 后退            │  0     │  -     │  0     │  -     │  -     │  -     │  -     │
 *    │ 右平移          │  +     │  0     │  0     │  -     │  +     │  +     │  -     │
 *    │ 左平移          │  -     │  0     │  0     │  +     │  -     │  -     │  +     │
 *    │ 逆时针旋转      │  0     │  0     │  +     │  -     │  +     │  -     │  +     │
 *    │ 顺时针旋转      │  0     │  0     │  -     │  +     │  -     │  +     │  -     │
 *    │ 右前45°斜移     │  +     │  +     │  0     │  0     │  ++    │  ++    │  0     │
 *    │ 左前45°斜移     │  -     │  +     │  0     │  ++    │  0     │  0     │  ++    │
 *    │ 右后45°斜移     │  +     │  -     │  0     │  --    │  0     │  0     │  --    │
 *    │ 左后45°斜移     │  -     │  -     │  0     │  0     │  --    │  --    │  0     │
 *    │ 前进+顺时针     │  0     │  +     │  -     │  ++    │  0     │  0     │  ++    │
 *    └─────────────────┴────────┴────────┴────────┴────────┴────────┴────────┘
 *
 *    "+" 表示正转, "-" 表示反转, "++" 表示两倍速正转, "0" 表示停止
 *
 *
 * ============================================================================================
 *                                使用方法
 * ============================================================================================
 *
 * 一、初始化配置 (在 main.c 中完成)
 *
 *     void main(void)
 *     {
 *         clock_init(SYSTEM_CLOCK_600M);
 *         MyPWM_Init();       // 1. 先初始化PWM (提供动力输出通道)
 *         Motor_Init();       // 2. 再初始化电机方向引脚
 *         // ... 其他初始化 ...
 *     }
 *
 *
 * 二、核心函数调用
 *
 *     // 全向移动: Mecanum_Move(vx, vy, omega)
 *     //   vx:    [-10000, +10000]  正值向右, 负值向左
 *     //   vy:    [-10000, +10000]  正值向前, 负值向后
 *     //   omega: [-10000, +10000]  正值逆时针, 负值顺时针
 *
 *     Mecanum_Move(0, 5000, 0);       // 以50%速度前进
 *     Mecanum_Move(0, -5000, 0);      // 以50%速度后退
 *     Mecanum_Move(5000, 0, 0);       // 以50%速度纯右平移
 *     Mecanum_Move(-5000, 0, 0);      // 以50%速度纯左平移
 *     Mecanum_Move(0, 0, 3000);       // 以30%速度原地逆时针旋转
 *     Mecanum_Move(0, 0, -3000);      // 以30%速度原地顺时针旋转
 *     Mecanum_Move(3000, 3000, 0);    // 右前方45°斜移
 *     Mecanum_Move(-3000, 3000, 0);   // 左前方45°斜移
 *     Mecanum_Move(3000, 5000, -1000);// 右前方移动 + 轻微顺时针修正
 *     Mecanum_Move(0, 0, 0);          // 停车
 *
 *
 * 三、便捷函数调用
 *
 *     Car_Forward(5000);      // 以50%速度前进
 *     Car_Backward(5000);     // 以50%速度后退
 *     Car_Left(5000);         // 以50%速度纯左平移
 *     Car_Right(5000);        // 以50%速度纯右平移
 *     Car_FrontLeft(5000);    // 以50%速度左前45°斜移
 *     Car_FrontRight(5000);   // 以50%速度右前45°斜移
 *     Car_RearLeft(5000);     // 以50%速度左后45°斜移
 *     Car_RearRight(5000);    // 以50%速度右后45°斜移
 *     Car_Rotate_CCW(3000);   // 以30%速度原地逆时针旋转
 *     Car_Rotate_CW(3000);    // 以30%速度原地顺时针旋转
 *     Motor_Stop();           // 紧急停车
 *
 *
 * 四、综合运动示例 (FreeRTOS任务中)
 *
 *     void Motion_Task(void *param)
 *     {
 *         while (1)
 *         {
 *             // 示例: 正方形轨迹
 *             Car_Forward(4000);              // 前进
 *             vTaskDelay(pdMS_TO_TICKS(2000));
 *
 *             Car_Right(4000);                // 右平移
 *             vTaskDelay(pdMS_TO_TICKS(2000));
 *
 *             Car_Backward(4000);             // 后退
 *             vTaskDelay(pdMS_TO_TICKS(2000));
 *
 *             Car_Left(4000);                 // 左平移
 *             vTaskDelay(pdMS_TO_TICKS(2000));
 *
 *             Motor_Stop();                   // 停车
 *             vTaskDelay(pdMS_TO_TICKS(1000));
 *         }
 *     }
 *
 ********************************************************************************************************************/

#include "Motor.h"
#include "PWM.h"

/*********************************************************************************************************************
 * 函数名称: Motor_Init
 * 函数功能: 初始化4个电机的方向控制GPIO引脚
 * 参数说明: 无
 * 返回值:   无
 *
 * 说明:
 *   将 L1/L2/R1/R2 四个电机的 DIR 引脚初始化为推挽输出, 默认低电平 (停止状态)。
 *   调用此函数前必须先完成系统时钟初始化。
 ********************************************************************************************************************/
void Motor_Init(void)
{
    gpio_init(MOTOR_L1_DIR, GPO, 0, GPO_PUSH_PULL);
    gpio_init(MOTOR_L2_DIR, GPO, 0, GPO_PUSH_PULL);
    gpio_init(MOTOR_R1_DIR, GPO, 0, GPO_PUSH_PULL);
    gpio_init(MOTOR_R2_DIR, GPO, 0, GPO_PUSH_PULL);
}

/*********************************************************************************************************************
 * 函数名称: Motor_Clamp
 * 函数功能: 将整数值限幅到 [min, max] 范围内
 * 参数说明:
 *   value  待限幅的值
 *   min    下限值
 *   max    上限值
 * 返回值:   限幅后的值
 ********************************************************************************************************************/
static int32 Motor_Clamp(int32 value, int32 min, int32 max)
{
    if (value > max) return max;
    if (value < min) return min;
    return value;
}

/*********************************************************************************************************************
 * 函数名称: Motor_Set
 * 函数功能: 设置单个电机的转速和方向
 * 参数说明:
 *   dir_pin   电机方向控制引脚, 取值: MOTOR_L1_DIR / MOTOR_L2_DIR / MOTOR_R1_DIR / MOTOR_R2_DIR
 *   pwm_pin   电机PWM输出通道, 取值: PWM_CH1_L1 / PWM_CH2_R1 / PWM_CH3_L2 / PWM_CH4_R2
 *   speed     电机速度, 范围 [-10000, +10000]
 *             正值 → DIR=1 (正转), 负值 → DIR=0 (反转)
 *             绝对值为PWM占空比 (0=停止, 10000=满速)
 * 返回值:     无
 *
 * 说明:
 *   根据 speed 的符号自动设置方向引脚, 绝对值设置占空比。
 *   speed 会被内部限幅到 [-10000, +10000]。
 ********************************************************************************************************************/
void Motor_Set(gpio_pin_enum dir_pin, pwm_channel_enum pwm_pin, int32 speed)
{
    speed = Motor_Clamp(speed, -MOTOR_PWM_MAX, MOTOR_PWM_MAX);

    if (speed >= 0)
    {
        gpio_set_level(dir_pin, 1);
        pwm_set_duty(pwm_pin, (uint32)speed);
    }
    else
    {
        gpio_set_level(dir_pin, 0);
        pwm_set_duty(pwm_pin, (uint32)(-speed));
    }
}

/*********************************************************************************************************************
 * 函数名称: Mecanum_Move
 * 函数功能: 麦克纳姆轮全向运动控制 (核心函数)
 * 参数说明:
 *   vx      X轴速度分量 (左右方向)
 *           范围: [-10000, +10000], 单位: PWM占空比当量
 *           正值 = 向右平移, 负值 = 向左平移
 *
 *   vy      Y轴速度分量 (前后方向)
 *           范围: [-10000, +10000], 单位: PWM占空比当量
 *           正值 = 向前运动, 负值 = 向后运动
 *
 *   omega   旋转角速度分量
 *           范围: [-10000, +10000], 单位: PWM占空比当量
 *           正值 = 逆时针旋转 (俯视), 负值 = 顺时针旋转
 *
 * 返回值:   无
 *
 * 运动学公式 (麦克纳姆轮逆运动学, O型安装):
 *   V_L1 = vy - vx - omega    (左前轮)
 *   V_R1 = vy + vx + omega    (右前轮)
 *   V_L2 = vy + vx - omega    (左后轮)
 *   V_R2 = vy - vx + omega    (右后轮)
 *
 * 等比例缩放:
 *   当任一轮的计算速度超过 PWM_MAX (10000) 时, 所有轮速按比例缩放,
 *   保证运动方向不变的前提下, 将最大轮速限制在 PWM_MAX 范围内。
 ********************************************************************************************************************/
void Mecanum_Move(int32 vx, int32 vy, int32 omega)
{
    /* 输入参数限幅 */
    vx    = Motor_Clamp(vx,    -MOTOR_PWM_MAX, MOTOR_PWM_MAX);
    vy    = Motor_Clamp(vy,    -MOTOR_PWM_MAX, MOTOR_PWM_MAX);
    omega = Motor_Clamp(omega, -MOTOR_PWM_MAX, MOTOR_PWM_MAX);

    /* ==================== 逆运动学计算 (O型安装) ==================== */
    int32 speed_L1 = vy - vx - omega;    /* 左前轮 */
    int32 speed_R1 = vy + vx + omega;    /* 右前轮 */
    int32 speed_L2 = vy + vx - omega;    /* 左后轮 */
    int32 speed_R2 = vy - vx + omega;    /* 右后轮 */

    /* ==================== 等比例缩放 ==================== */
    /* 找到四个轮速的绝对值最大值 */
    int32 abs_L1 = (speed_L1 >= 0) ? speed_L1 : -speed_L1;
    int32 abs_R1 = (speed_R1 >= 0) ? speed_R1 : -speed_R1;
    int32 abs_L2 = (speed_L2 >= 0) ? speed_L2 : -speed_L2;
    int32 abs_R2 = (speed_R2 >= 0) ? speed_R2 : -speed_R2;

    int32 max_speed = abs_L1;
    if (abs_R1 > max_speed) max_speed = abs_R1;
    if (abs_L2 > max_speed) max_speed = abs_L2;
    if (abs_R2 > max_speed) max_speed = abs_R2;

    /* 若最大轮速超过 PWM_MAX, 等比例缩放所有轮速 */
    if (max_speed > MOTOR_PWM_MAX)
    {
        speed_L1 = speed_L1 * MOTOR_PWM_MAX / max_speed;
        speed_R1 = speed_R1 * MOTOR_PWM_MAX / max_speed;
        speed_L2 = speed_L2 * MOTOR_PWM_MAX / max_speed;
        speed_R2 = speed_R2 * MOTOR_PWM_MAX / max_speed;
    }

    /* ==================== 设置各电机速度 ==================== */
    Motor_Set(MOTOR_L1_DIR, PWM_CH1_L1, speed_L1);
    Motor_Set(MOTOR_R1_DIR, PWM_CH2_R1, speed_R1);
    Motor_Set(MOTOR_L2_DIR, PWM_CH3_L2, speed_L2);
    Motor_Set(MOTOR_R2_DIR, PWM_CH4_R2, speed_R2);
}

/*********************************************************************************************************************
 * 函数名称: Motor_Stop
 * 函数功能: 紧急停车, 所有电机PWM置零
 * 参数说明: 无
 * 返回值:   无
 ********************************************************************************************************************/
void Motor_Stop(void)
{
    Mecanum_Move(0, 0, 0);
}

/* ============================================================================================
 *                              便捷运动函数
 *
 *   以下函数是对 Mecanum_Move() 的封装, 提供更直观的调用方式。
 *   speed 参数范围: [0, 10000], 表示运动速度的PWM占空比当量。
 *
 * ============================================================================================ */

/*********************************************************************************************************************
 * 函数名称: Car_Forward
 * 函数功能: 前进
 * 参数说明:
 *   speed   速度, 范围 [0, 10000], 0=停止, 10000=满速
 ********************************************************************************************************************/
void Car_Forward(int32 speed)
{
    Mecanum_Move(0, speed, 0);
}

/*********************************************************************************************************************
 * 函数名称: Car_Backward
 * 函数功能: 后退
 * 参数说明:
 *   speed   速度, 范围 [0, 10000], 0=停止, 10000=满速
 ********************************************************************************************************************/
void Car_Backward(int32 speed)
{
    Mecanum_Move(0, -speed, 0);
}

/*********************************************************************************************************************
 * 函数名称: Car_Right
 * 函数功能: 纯右平移 (车头方向不变, 整车向左平移)
 * 参数说明:
 *   speed   速度, 范围 [0, 10000], 0=停止, 10000=满速
 *
 * 轮速分析 (vx=-speed, vy=0, omega=0):
 *   L1 = +speed (正转)    R1 = -speed (反转)
 *   L2 = -speed (反转)    R2 = +speed (正转)
 *   对角轮同向, 相邻轮反向 → 纯左平移
 ********************************************************************************************************************/
void Car_Right(int32 speed)
{
    Mecanum_Move(-speed, 0, 0);
}

/*********************************************************************************************************************
 * 函数名称: Car_Left
 * 函数功能: 纯左平移 (车头方向不变, 整车向右平移)
 * 参数说明:
 *   speed   速度, 范围 [0, 10000], 0=停止, 10000=满速
 *
 * 轮速分析 (vx=+speed, vy=0, omega=0):
 *   L1 = -speed (反转)    R1 = +speed (正转)
 *   L2 = +speed (正转)    R2 = -speed (反转)
 *   对角轮同向, 相邻轮反向 → 纯右平移
 ********************************************************************************************************************/
void Car_Left(int32 speed)
{
    Mecanum_Move(speed, 0, 0);
}

/*********************************************************************************************************************
 * 函数名称: Car_FrontLeft
 * 函数功能: 左前方 45° 斜移
 * 参数说明:
 *   speed   速度, 范围 [0, 10000], 0=停止, 10000=满速
 *
 * 轮速分析 (vx=-speed, vy=+speed, omega=0):
 *   L1 = speed + speed = 2·speed (正转)    R1 = -speed + speed = 0
 *   L2 = -speed + speed = 0                R2 = speed + speed = 2·speed (正转)
 *   R1和L2停止, L1和R2正转 → 左前45°平移 (等比例缩放后实际为speed)
 ********************************************************************************************************************/
void Car_FrontLeft(int32 speed)
{
    Mecanum_Move(-speed, speed, 0);
}

/*********************************************************************************************************************
 * 函数名称: Car_FrontRight
 * 函数功能: 右前方 45° 斜移
 * 参数说明:
 *   speed   速度, 范围 [0, 10000], 0=停止, 10000=满速
 *
 * 轮速分析 (vx=+speed, vy=+speed, omega=0):
 *   L1 = speed - speed = 0                 R1 = speed + speed = 2·speed (正转)
 *   L2 = speed + speed = 2·speed (正转)    R2 = speed - speed = 0
 *   L1和R2停止, R1和L2正转 → 右前45°平移 (等比例缩放后实际为speed)
 ********************************************************************************************************************/
void Car_FrontRight(int32 speed)
{
    Mecanum_Move(speed, speed, 0);
}

/*********************************************************************************************************************
 * 函数名称: Car_RearLeft
 * 函数功能: 左后方 45° 斜移
 * 参数说明:
 *   speed   速度, 范围 [0, 10000], 0=停止, 10000=满速
 ********************************************************************************************************************/
void Car_RearLeft(int32 speed)
{
    Mecanum_Move(-speed, -speed, 0);
}

/*********************************************************************************************************************
 * 函数名称: Car_RearRight
 * 函数功能: 右后方 45° 斜移
 * 参数说明:
 *   speed   速度, 范围 [0, 10000], 0=停止, 10000=满速
 ********************************************************************************************************************/
void Car_RearRight(int32 speed)
{
    Mecanum_Move(speed, -speed, 0);
}

/*********************************************************************************************************************
 * 函数名称: Car_Rotate_CCW
 * 函数功能: 原地逆时针旋转 (俯视)
 * 参数说明:
 *   speed   旋转速度, 范围 [0, 10000], 0=停止, 10000=满速
 *
 * 轮速分析 (vx=0, vy=0, omega=+speed):
 *   L1 = -speed (反转)    R1 = +speed (正转)
 *   L2 = -speed (反转)    R2 = +speed (正转)
 *   左侧轮反转, 右侧轮正转 → 原地逆时针旋转
 ********************************************************************************************************************/
void Car_Rotate_CCW(int32 speed)
{
    Mecanum_Move(0, 0, speed);
}

/*********************************************************************************************************************
 * 函数名称: Car_Rotate_CW
 * 函数功能: 原地顺时针旋转 (俯视)
 * 参数说明:
 *   speed   旋转速度, 范围 [0, 10000], 0=停止, 10000=满速
 *
 * 轮速分析 (vx=0, vy=0, omega=-speed):
 *   L1 = +speed (正转)    R1 = -speed (反转)
 *   L2 = +speed (正转)    R2 = -speed (反转)
 *   左侧轮正转, 右侧轮反转 → 原地顺时针旋转
 ********************************************************************************************************************/
void Car_Rotate_CW(int32 speed)
{
    Mecanum_Move(0, 0, -speed);
}
